package lesson10;

public class CowFarm {

    private int amountOfCow;
    private double farmPrice;

    public void setFarmPrice(double farmPrice) {
        this.farmPrice = farmPrice;
    }

    public double getFarmPrice() {
        return farmPrice;
    }

    public void setAmountOfCow(int amountOfCow) {
        this.amountOfCow = amountOfCow;
    }

    public int getAmountOfCow() {
        return amountOfCow;
    }
}
