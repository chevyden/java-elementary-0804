package lesson10;

public class Cow {

    private double produceMilkPerDay;
    private double consumeFeedPerDay;

    public void setProduceMilkPerDay(double produceMilkPerDay) {
        this.produceMilkPerDay = produceMilkPerDay;
    }

    public double getProduceMilkPerDay() {
        return produceMilkPerDay;
    }

    public void setConsumeFeedPerDay(double consumeFeedPerDay) {
        this.consumeFeedPerDay = consumeFeedPerDay;
    }

    public double getConsumeFeedPerDay() {
        return consumeFeedPerDay;
    }
}
